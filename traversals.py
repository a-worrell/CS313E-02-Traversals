# Iterates over a 2D list from left to right, then top to bottom
# and returning the coordinates (row, column).
def row_major_traversal(grid):
    row = 0
    loc = []
    while row < len(grid):
        col = 0
        while col < len(grid[row]):
            loc.append((row, col))
            col += 1
        row += 1
    return loc


